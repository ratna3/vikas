// Helper function to simulate async data fetching
export const getMockBlogs = (published = true) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      const filteredBlogs = published 
        ? mockBlogs.filter(blog => blog.published)
        : mockBlogs;
      resolve(filteredBlogs);
    }, 500); // Simulate network delay
  });
};

